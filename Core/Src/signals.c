//Find out the mean 
 float32_t signal_mean(float32_t *sig_src_arr, uint32_t sig_length)
{
  float32_t mean=0;
  for(uint32_t i=0; i<sig_length;i++ )
    mean=mean+sig_src_arr[i];
  
  mean=mean/(float32_t)sig_length;

  return mean;

}

//Find out the standard deviation
float32_t signal_variance(float32_t *sig_src_arr,float32_t sig_mean, uint32_t sig_length)
{
    float32_t variance=0.0;
    for(uint32_t i=0; i<sig_length;i++ )
      variance = variance + powf((sig_src_arr[i] - sig_mean),2);
    
    variance= variance/(sig_length-1);

    return variance;
}

 float32_t signal_standard_deviation(float32_t sig_variance)
{
  return sqrt(sig_variance);
}

